export class OrderStatusUtil{

	static getOrderStatus(num){
		switch (num){
			case "1":
				return "Refunding";
				break;
			case "2":
				return "Changing";
				break;
			case "3":
				return "Refunded";
				break;
			case "4":
				return "Changed";
				break;
			case "105":
			    return "Upcoming";
			    break;
			case "106":
				return "Complete";
				break;
			case "107":
				return "To be paid";
				break;    	
		}
	}
}